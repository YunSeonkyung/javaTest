


package kr.co.job.exec;

public class ChpaFour {

	public static void main(String[] args) {
		// 선언
		// 만일 문자열이 "123456"라면 각 자리수의 합을 구하기
		// '1'-'0' => 1
		String str = "12345";
		System.out.println("str문자열의 길이 : + str.length()");
		System.out.println(str.charAt(0));
		System.out.println(str.charAt(1));
		System.out.println(str.charAt(2));
		System.out.println(str.charAt(3));
		System.out.println(str.charAt(4)); // str.length()-1;
		System.out.println(str.charAt(5)); // str.length();
		String s = "Hello";
		String s2 = s.concat("world");
		System.out.println(s2);
		
		

	}

}
